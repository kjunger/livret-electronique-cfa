<h1>Contrat pédagogique</h1>
<div>
    <span>L'apprenti(e) s'engage à :</span>
    <ul>
        <li>Travailler pour son employeur et effectuer les missions confiées dans le but d'acquérier des compétences ;</li>
        <li>Respecter le règlement intérier de l'entreprise ;</li>
        <li>Suivre les cours et activités du programme de la formation en vue d'obtenir le diplôme visé ;</li>
        <li>Respecter le règlement intérieur de l'établissement de rattachement et respecter le calendrier et les échéances fixées ;</li>
        <li><strong>Tenir à jour son livret de suivi. La bonne tenue dudit livret et le respect des échéances feront l'objet d'une évaluation.</strong></li>
    </ul>
    <span>L'entreprise s'engage à :</span>
    <ul>
        <li>Assurer à l'apprenti(e) une formation méthodique et complète pour le métier prévu au contrat ;</li>
        <li>Verser un salaire à l'apprenti(e) conformément au contrat signé entre les parties ;</li>
        <li>Employer l'apprenti(e) conformément à la législation en vigueur ;</li>
        <li>Faire suivre à l'apprenti(e) toutes les formations et activités pédagogiques de l'établissement de rattachement ;</li>
        <li>Veiller à la bonne tenue du livret de suivi ;</li>
        <li>Veiller à ce que l'apprenti(e) se présente aux évaluations prévues.</li>
    </ul>
    <span>L'établissement de rattachement s'engage à :</span>
    <ul>
        <li>Assurer à l'apprenti(e) un enseignement général et technique correspond au programme pédagogique du diplôme préparé ;</li>
        <li>Assurer la coordination pédagogique entre l'entreprise et l'établissement ;</li>
        <li>Suivre l'assiduité, le travail et la progression de l'acquisition des compétences.</li>
    </ul>
</div>